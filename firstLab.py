# num1 السطر التالي يطلب من المستخدم إدخال الرقم الأول ويخزنه في المتغير 
num1 = int(input("الرجاء إدخال الرقم الأول: "))

# num2 السطر التالي يطلب من المستخدم إدخال الرقم الثاني ويخزنه في المتغير 
num2 = int(input("الرجاء إدخال الرقم الثاني: "))

# sum السطر التالي يقوم بجمع الرقمين num1 و num2 ويخزن الناتج في المتغير 
sum = num1 + num2

# السطر التالي يقوم بطباعة ناتج الجمع
print("مجموع الرقمين هو:", sum)
